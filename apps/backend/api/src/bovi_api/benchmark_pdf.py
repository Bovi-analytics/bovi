def _scatter_svg(
    x: list[float],
    y: list[float],
    title: str,
    xlabel: str,
    ylabel: str,
    figsize: tuple[float, float] = (5.0, 4.5),
    title_size: int = 11,
    point_size: float = 14.0,
) -> str:
    """Render a density-shaded scatter as SVG text (vector)."""
    fig, ax = plt.subplots(figsize=figsize)

    if x and y:
        lo = min(min(x), min(y)) * 0.97
        hi = max(max(x), max(y)) * 1.03
        ax.plot([lo, hi], [lo, hi], "--", color="#888", linewidth=1.0, label="y = x", zorder=1)
        ax.scatter(
            x,
            y,
            s=point_size,
            alpha=0.35,
            color="#0067ac",
            edgecolors="none",
            zorder=2,
        )
        ax.set_xlim(lo, hi)
        ax.set_ylim(lo, hi)
        ax.legend(loc="upper left", fontsize=8, frameon=False)

    ax.set_title(title, fontsize=title_size, color="#00588e", fontweight="bold", pad=6)
    ax.set_xlabel(xlabel, fontsize=9, color="#5f6e7d")
    ax.set_ylabel(ylabel, fontsize=9, color="#5f6e7d")
    ax.tick_params(labelsize=8, colors="#5f6e7d")
    for spine in ax.spines.values():
        spine.set_color("#dce2e8")
    ax.grid(True, alpha=0.30, linewidth=0.5, color="#dce2e8")
    ax.set_axisbelow(True)
    ax.set_aspect("equal", adjustable="box")

    buf = io.BytesIO()
    fig.savefig(buf, format="svg", bbox_inches="tight", facecolor="white")
    plt.close(fig)
    return buf.getvalue().decode("utf-8")


def _embed_svg(pdf: FPDF, svg_text: str, x: float, y: float, w: float) -> None:
    """Embed an SVG image at (x, y) with width w (mm)."""
    pdf.image(io.BytesIO(svg_text.encode("utf-8")), x=x, y=y, w=w)


def _fmt(v: Any) -> str:
    if v is None:
        return "-"
    return f"{v:.3f}" if isinstance(v, float) else str(v)


class BoviPDF(FPDF):
    """FPDF subclass with Bovi-branded footer."""

    generated_at: str = ""

    def footer(self) -> None:
        self.set_y(-12)
        self.set_font("Helvetica", "", 8)
        self.set_text_color(*GREY_TEXT)
        self.cell(
            0,
            5,
            "Generated by Bovi  -  dairy analytics platform",
            align="L",
            new_x="LMARGIN",
            new_y="NEXT",
        )
        self.set_y(-12)
        self.set_font("Helvetica", "I", 8)
        self.cell(0, 5, f"Page {self.page_no()}/{{nb}}   {self.generated_at}", align="R")


def _section_header(pdf: BoviPDF, num: int, title: str) -> None:
    """Numbered badge + title + cyan underline."""
    pdf.set_y(20)
    y = pdf.get_y()
    pdf.set_fill_color(*PRIMARY)
    pdf.set_text_color(255, 255, 255)
    pdf.set_font("Helvetica", "B", 12)
    pdf.set_xy(LEFT_MARGIN, y)
    pdf.cell(8, 8, str(num), align="C", fill=True)
    pdf.set_xy(LEFT_MARGIN + 10, y)
    pdf.set_text_color(*NAVY)
    pdf.set_font("Helvetica", "B", 14)
    pdf.cell(0, 8, title, new_x="LMARGIN", new_y="NEXT")
    pdf.set_draw_color(*ACCENT)
    pdf.set_line_width(0.6)
    pdf.line(LEFT_MARGIN, pdf.get_y() + 0.5, PAGE_W - RIGHT_MARGIN, pdf.get_y() + 0.5)
    pdf.set_line_width(0.2)
    pdf.ln(5)


def _compact_metrics_table(
    pdf: BoviPDF,
    block: dict,
    parity_keys: list[str],
    x: float,
    width: float,
) -> float:
    """Compact metrics table starting at x with given width. Returns y after table."""
    headers = ["Group", "N", "Pearson", "RMSE", "MAE", "MAPE"]
    weights = [1.6, 0.7, 1.0, 1.0, 1.0, 1.0]
    total_w = sum(weights)
    col_widths = [width * w / total_w for w in weights]
    row_h = 6.5

    pdf.set_xy(x, pdf.get_y())
    pdf.set_fill_color(*NAVY)
    pdf.set_text_color(255, 255, 255)
    pdf.set_font("Helvetica", "B", 9)
    for h, w in zip(headers, col_widths):
        pdf.cell(w, row_h, h, border=0, fill=True, align="C")
    pdf.ln()

    pdf.set_text_color(40, 50, 60)
    pdf.set_font("Helvetica", "", 9)
    rows: list[tuple[str, dict]] = [("Overall", block.get("overall", {}))]
    by_parity = block.get("by_parity", {})
    for p in parity_keys:
        if p in by_parity:
            rows.append((f"Parity {p}", by_parity[p]))

    for i, (label, s) in enumerate(rows):
        striped = i % 2 == 1
        if striped:
            pdf.set_fill_color(*GREY_BG)
        cells = [
            label,
            str(s.get("n", 0)),
            _fmt(s.get("pearson")),
            _fmt(s.get("rmse")),
            _fmt(s.get("mae")),
            _fmt(s.get("mape")),
        ]
        is_overall = label == "Overall"
        if is_overall:
            pdf.set_font("Helvetica", "B", 9)
        pdf.set_x(x)
        for j, (val, w) in enumerate(zip(cells, col_widths)):
            align = "L" if j == 0 else "C"
            pdf.cell(w, row_h, val, border=0, fill=striped, align=align)
        if is_overall:
            pdf.set_font("Helvetica", "", 9)
        pdf.ln()

    pdf.set_draw_color(*GREY_LINE)
    pdf.line(x, pdf.get_y(), x + sum(col_widths), pdf.get_y())
    return pdf.get_y()


def _pairs(
    a: dict[str, float],
    b: dict[str, float],
    parities: dict[str, int] | None = None,
    parity_filter: str | None = None,
) -> tuple[list[float], list[float]]:
    common = [cid for cid in a if cid in b]
    if parity_filter is not None and parities is not None:

        def _key(cid: str) -> str:
            p = parities.get(cid, 1)
            return str(p) if p <= 2 else "3+"

        common = [cid for cid in common if _key(cid) == parity_filter]
    return [a[cid] for cid in common], [b[cid] for cid in common]


def _draw_cover(
    pdf: BoviPDF,
    challenge_name: str,
    challenge_source: str,
    challenger_label: str,
    benchmark_label: str,
    n_evaluated: int,
    n_excluded: int,
) -> None:
    """Cover page: logo + title, cohort metadata, intro, glossary."""
    if LOGO_PATH.exists():
        try:
            pdf.image(str(LOGO_PATH), x=15, y=14, h=12)
        except Exception:  # noqa: BLE001
            pass

    pdf.set_xy(0, 14)
    pdf.set_text_color(*NAVY)
    pdf.set_font("Helvetica", "B", 18)
    pdf.cell(0, 8, "Benchmark Report", align="R")
    pdf.set_xy(0, 22)
    pdf.set_text_color(*GREY_TEXT)
    pdf.set_font("Helvetica", "", 9)
    pdf.cell(0, 5, "Lactation calculation evaluation", align="R")
    pdf.set_x(-15)

    # Cyan accent line under header
    pdf.set_draw_color(*ACCENT)
    pdf.set_line_width(0.6)
    pdf.line(LEFT_MARGIN, 30, PAGE_W - RIGHT_MARGIN, 30)
    pdf.set_line_width(0.2)

    pdf.set_y(38)
    pdf.set_text_color(*NAVY)
    pdf.set_font("Helvetica", "B", 16)
    pdf.cell(0, 8, challenge_name or "Cohort", new_x="LMARGIN", new_y="NEXT")

    def _meta(label: str, value: str) -> None:
        pdf.set_font("Helvetica", "B", 9.5)
        pdf.set_text_color(*NAVY)
        pdf.cell(34, 6, label)
        pdf.set_font("Helvetica", "", 9.5)
        pdf.set_text_color(40, 50, 60)
        pdf.cell(0, 6, value, new_x="LMARGIN", new_y="NEXT")

    _meta("Source", challenge_source)
    _meta("Challenger", challenger_label)
    _meta("Benchmark", benchmark_label)
    _meta("Cows evaluated", str(n_evaluated))
    if n_excluded:
        _meta("Excluded", str(n_excluded))
    pdf.ln(4)

    pdf.set_x(LEFT_MARGIN)
    pdf.set_text_color(*NAVY)
    pdf.set_font("Helvetica", "B", 11)
    pdf.cell(0, 6, "How to read this report", new_x="LMARGIN", new_y="NEXT")
    pdf.set_text_color(40, 50, 60)
    pdf.set_font("Helvetica", "", 9.5)
    intro = (
        "Each scatter plot below shows one point per cow in the cohort. The horizontal "
        "axis is the ground-truth Actual Lactation Yield (ALY) - the cumulative 305-day "
        "milk production from daily-meter recordings. The vertical axis is the model's "
        "calculated 305-day yield for the same cow, derived from the sparse test-day "
        "records. A perfect model places every point on the dashed diagonal (y = x). "
        "Points above the line indicate over-estimation, points below indicate "
        "under-estimation. Overlapping points appear darker so denser regions of the "
        "cloud stand out."
    )
    pdf.multi_cell(0, 5, intro)
    pdf.ln(3)

    pdf.set_x(LEFT_MARGIN)
    pdf.set_text_color(*NAVY)
    pdf.set_font("Helvetica", "B", 11)
    pdf.cell(0, 6, "Glossary", new_x="LMARGIN", new_y="NEXT")
    pdf.set_text_color(40, 50, 60)
    pdf.set_font("Helvetica", "", 9.5)
    pdf.multi_cell(
        0,
        5,
        "ALY    Actual Lactation Yield - daily-meter ground truth (kg).\n"
        "SCALY  Submitted Calculated ALY - challenger's 305-day output.\n"
        "BCALY  Benchmark Calculated ALY - benchmark model's 305-day output.",
    )

    pdf.set_x(LEFT_MARGIN)
    pdf.ln(4)
    pdf.set_text_color(*NAVY)
    pdf.set_font("Helvetica", "B", 11)
    pdf.cell(0, 6, "What follows", new_x="LMARGIN", new_y="NEXT")
    pdf.set_text_color(40, 50, 60)
    pdf.set_font("Helvetica", "", 9.5)
    pdf.multi_cell(
        0,
        5,
        "1. Challenger vs ALY  -  the main result.\n"
        "2. Benchmark vs ALY   -  baseline performance.\n"
        "3. Challenger vs Benchmark  -  inter-model agreement.\n"
        "Each section shows overall metrics + scatter, then a per-parity breakdown.",
    )


def _render_section(
    pdf: BoviPDF,
    num: int,
    title: str,
    block: dict,
    a: dict[str, float],
    b: dict[str, float],
    parities: dict[str, int],
    overall_title: str,
    xlabel: str,
    ylabel: str,
) -> None:
    """One section per page: header, table+overall scatter side-by-side, parity strip below.

    `a` is x-axis (e.g. ALY), `b` is y-axis (e.g. challenger).
    """
    parity_keys = ["1", "2", "3+"]
    pdf.add_page()
    _section_header(pdf, num, title)

    # Two-column row: table left (vertically centered), scatter right
    top_y = pdf.get_y()
    table_w = 86.0
    scatter_w = USABLE_W - table_w - 6.0
    scatter_x = LEFT_MARGIN + table_w + 6.0

    # Scatter
    x_pts, y_pts = _pairs(a, b)
    scatter_h = scatter_w * (4.6 / 5.0)  # follows figsize ratio
    if x_pts:
        svg = _scatter_svg(
            x_pts,
            y_pts,
            title=overall_title,
            xlabel=xlabel,
            ylabel=ylabel,
            figsize=(5.0, 4.6),
            title_size=11,
            point_size=14,
        )
        _embed_svg(pdf, svg, x=scatter_x, y=top_y, w=scatter_w)

    # Estimate table height: header + rows
    n_rows = 1 + sum(1 for p in parity_keys if p in block.get("by_parity", {}))
    row_h = 6.5
    table_h = (1 + n_rows) * row_h  # header + rows
    table_y = top_y + max(0.0, (scatter_h - table_h) / 2)

    # Render the table at the centered y
    pdf.set_xy(LEFT_MARGIN, table_y)
    _compact_metrics_table(pdf, block, parity_keys, x=LEFT_MARGIN, width=table_w)

    pdf.set_y(top_y + scatter_h + 6)

    # Per-parity breakdown title
    pdf.set_x(LEFT_MARGIN)
    pdf.set_text_color(*NAVY)
    pdf.set_font("Helvetica", "B", 10.5)
    pdf.cell(0, 6, "Breakdown by parity", new_x="LMARGIN", new_y="NEXT")
    pdf.ln(1)

    # Three small scatters side by side (vectors!)
    width = (USABLE_W - 2 * 4.0) / 3.0
    y_top = pdf.get_y()
    for i, p in enumerate(parity_keys):
        x_p, y_p = _pairs(a, b, parities, parity_filter=p)
        if not x_p:
            continue
        svg = _scatter_svg(
            x_p,
            y_p,
            title=f"Parity {p}  (n={len(x_p)})",
            xlabel=xlabel,
            ylabel=ylabel,
            figsize=(4.0, 3.6),
            title_size=10,
            point_size=12,
        )
        x_pos = LEFT_MARGIN + i * (width + 4.0)
        _embed_svg(pdf, svg, x=x_pos, y=y_top, w=width)


def generate_report_pdf(
    stats: dict,
    challenger_yields: dict[str, float],
    benchmark_yields: dict[str, float],
    actual_yields: dict[str, float],
    parities: dict[str, int],
    challenge_name: str = "",
    challenge_source: str = "",
    submission_type: str | None = None,
    challenger_label: str = "challenger",
    benchmark_label: str = "benchmark",
) -> bytes:
    """Generate a Bovi-branded three-section PDF benchmark report."""
    pdf = BoviPDF()
    pdf.generated_at = datetime.now(tz=timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
    pdf.set_auto_page_break(auto=True, margin=18)
    pdf.alias_nb_pages()
    pdf.set_margins(LEFT_MARGIN, 32, RIGHT_MARGIN)
    pdf.add_page()

    overall_n = stats.get("challenger_vs_aly", {}).get("overall", {}).get("n", 0)
    _draw_cover(
        pdf,
        challenge_name=challenge_name,
        challenge_source=challenge_source,
        challenger_label=challenger_label,
        benchmark_label=benchmark_label,
        n_evaluated=overall_n,
        n_excluded=stats.get("failed_count", 0) or 0,
    )

    # Section 1: Challenger vs ALY (x = ALY, y = challenger)
    _render_section(
        pdf,
        1,
        f"Challenger ({challenger_label}) vs Actual yield",
        stats.get("challenger_vs_aly", {}),
        actual_yields,
        challenger_yields,
        parities,
        overall_title="SCALY vs ALY  (overall)",
        xlabel="ALY (kg)",
        ylabel="SCALY (kg)",
    )

    # Section 2: Benchmark vs ALY
    _render_section(
        pdf,
        2,
        f"Benchmark ({benchmark_label}) vs Actual yield",
        stats.get("benchmark_vs_aly", {}),
        actual_yields,
        benchmark_yields,
        parities,
        overall_title="BCALY vs ALY  (overall)",
        xlabel="ALY (kg)",
        ylabel="BCALY (kg)",
    )

    # Section 3: Challenger vs Benchmark agreement
    _render_section(
        pdf,
        3,
        "Challenger vs Benchmark agreement",
        stats.get("challenger_vs_benchmark", {}),
        benchmark_yields,
        challenger_yields,
        parities,
        overall_title="SCALY vs BCALY  (overall)",
        xlabel="BCALY (kg)",
        ylabel="SCALY (kg)",
    )

    _draw_appendix(pdf)

    buf = io.BytesIO()
    pdf.output(buf)
    return buf.getvalue()


